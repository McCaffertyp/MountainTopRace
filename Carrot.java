public class Carrot {
    int row, col;
        
    Carrot() {
    }
        
    Carrot(int assignRow, int assignCol) {
        row = assignRow;
        col = assignCol;
    }
        
    //Getter methods
    public int getRow() {
        return row;
    }
    
    public int getCol() {
        return col;
    }
} //Carrot class end
